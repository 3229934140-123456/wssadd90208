function formatDate(d: Date): string {
  return d.toISOString().split('T')[0];
}

function formatDateTime(d: Date): string {
  return d.toISOString();
}

function addDays(dateStr: string, days: number): string {
  const d = new Date(dateStr);
  d.setDate(d.getDate() + days);
  return d.toISOString();
}

function generateCustomerRecords(count: number): CustomerRecord[] {
  const records: CustomerRecord[] = [];
  const baseDate = new Date('2024-06-22');
  const projectIds = Object.keys(projectInfoMap);
  const storeIds = Object.keys(storeDoctorMap);
  const statuses: any[] = ['confirmed', 'confirmed', 'confirmed', 'confirmed', 'pending', 'reviewing', 'rejected'];

  for (let i = 0; i < count; i++) {
    const daysAgo = Math.floor(Math.random() * 60);
    const recordDate = new Date(baseDate);
    recordDate.setDate(recordDate.getDate() - daysAgo);

    const storeId = pickRandom(storeIds);
    const doctorId = pickRandom(storeDoctorMap[storeId]);
    const projectId = pickRandom(projectIds);
    const projInfo = projectInfoMap[projectId];
    const drugInfo = projectDrugMap[projectId];
    const status = pickRandom(statuses);

    const customerAge = 22 + Math.floor(Math.random() * 35);
    const customerName = pickRandom(firstNames) + pickRandom(lastNames);

    const recordNo = `REC${formatDate(recordDate).replace(/-/g, '')}${String(i + 1).padStart(4, '0')}`;

    const startTimeHour = 9 + Math.floor(Math.random() * 11);
    const startDate = new Date(recordDate);
    startDate.setHours(startTimeHour, 0, 0, 0);
    const endDate = new Date(startDate);
    endDate.setMinutes(endDate.getMinutes() + 30 + Math.floor(Math.random() * 45));

    const drugs: DrugUsage[] = drugInfo.map((d, idx) => ({
      id: `usage_${i}_${idx}`,
      recordId: `rec_${String(i + 1).padStart(4, '0')}`,
      drugId: d.drugId,
      drugName: d.drugName,
      drugBrand: d.brand,
      specification: d.spec,
      dosage: d.dosage,
      unit: d.unit,
      batchNo: d.batch,
      price: d.price,
      usedAt: formatDateTime(startDate),
      operatorId: doctorId,
      operatorName: doctorNameMap[doctorId],
    }));

    const template = projectTemplates.find(p => p.id === projectId);
    const injectionPoints = template ? template.injectionPoints.map(ip => ({
      pointId: ip.pointId,
      pointName: ip.pointName,
      actualDosage: ip.recommendedDosage,
      unit: ip.unit,
      depth: ip.pointId.startsWith('point_01') || ip.pointId.startsWith('point_00') ? 3 + Math.random() * 12 : undefined,
      angle: 30 + Math.floor(Math.random() * 60),
    })) : [];

    const totalAmount = template?.price || 2000;
    const paidStatus = pickRandom(['paid', 'paid', 'paid', 'partial', 'unpaid'] as const);
    const paidAmount = paidStatus === 'paid' ? totalAmount : paidStatus === 'partial' ? Math.floor(totalAmount * 0.5) : 0;

    let qualityScore: QualityScore | undefined;
    let riskPoints: RiskPoint[] | undefined;
    let followUps: FollowUpRecord[] | undefined;

    if (status === 'confirmed') {
      const scores = [
        { category: '术前评估', score: 18 + Math.floor(Math.random() * 3), fullScore: 20 },
        { category: '注射规范', score: 27 + Math.floor(Math.random() * 4), fullScore: 30 },
        { category: '无菌操作', score: 19 + Math.floor(Math.random() * 2), fullScore: 20 },
        { category: '术后告知', score: 17 + Math.floor(Math.random() * 4), fullScore: 20 },
        { category: '记录完整', score: 8 + Math.floor(Math.random() * 3), fullScore: 10 },
      ];
      const totalScore = scores.reduce((s, c) => s + c.score, 0);
      const rating = totalScore >= 90 ? 'excellent' : totalScore >= 80 ? 'good' : totalScore >= 70 ? 'fair' : 'poor';

      qualityScore = {
        id: `qs_${i}`,
        recordId: `rec_${String(i + 1).padStart(4, '0')}`,
        inspectorId: 'user_008',
        inspectorName: '质控张',
        totalScore,
        scores,
        overallRating: rating,
        remarks: rating === 'excellent' ? '操作规范，记录完整' : rating === 'good' ? '整体良好，细节可进一步优化' : rating === 'fair' ? '存在部分不规范，需改进' : '存在较多问题，需重点整改',
        evaluatedAt: addDays(formatDateTime(startDate), 1 + Math.floor(Math.random() * 3)),
        followUpRequired: rating !== 'excellent',
      };

      if (Math.random() < 0.3) {
        riskPoints = [];
        const riskCount = 1 + Math.floor(Math.random() * 2);
        const riskCategories = ['注射规范', '药品管理', '记录完整', '无菌操作', '患者告知'];
        const riskLevels: any[] = ['low', 'low', 'medium', 'medium', 'high'];
        for (let r = 0; r < riskCount; r++) {
          const level = pickRandom(riskLevels);
          riskPoints.push({
            id: `risk_${i}_${r}`,
            recordId: `rec_${String(i + 1).padStart(4, '0')}`,
            level,
            category: pickRandom(riskCategories),
            description: level === 'high' ? '存在较高风险，需立即整改' : level === 'medium' ? '存在一定风险，建议关注改进' : '轻微问题，持续关注',
            identifiedAt: addDays(formatDateTime(startDate), 1),
            identifiedBy: '质控张',
            status: pickRandom(['open', 'mitigated', 'resolved', 'resolved']),
            mitigationMeasures: level !== 'low' ? ['加强培训', '复核流程'] : undefined,
            resolvedAt: level !== 'low' ? addDays(formatDateTime(startDate), 3) : undefined,
            resolvedBy: level !== 'low' ? doctorNameMap[doctorId] : undefined,
          });
        }
      }

      if (Math.random() < 0.6) {
        followUps = [];
        const fuCount = 1 + Math.floor(Math.random() * 2);
        const fuTypes: any[] = ['phone', 'wechat', 'wechat', 'visit'];
        for (let f = 0; f < fuCount; f++) {
          const fuDays = [7, 30, 90][f] || 7;
          followUps.push({
            id: `fu_${i}_${f}`,
            recordId: `rec_${String(i + 1).padStart(4, '0')}`,
            customerId: `cust_${String(1000 + i).padStart(4, '0')}`,
            followUpDate: formatDate(new Date(addDays(formatDate(recordDate), fuDays))),
            type: pickRandom(fuTypes),
            content: '术后恢复情况回访，了解效果和不良反应',
            customerFeedback: pickRandom(['效果很好，非常满意', '恢复良好，效果自然', '有些肿，但在消退', '有点淤青，已消退', '效果超出预期']),
            satisfaction: 4 + Math.floor(Math.random() * 2),
            operatorId: 'user_006',
            operatorName: '王小护',
            nextFollowUpDate: f < 2 ? formatDate(new Date(addDays(formatDate(recordDate), fuDays * 2))) : undefined,
            hasAbnormality: Math.random() < 0.1,
            abnormalityDesc: Math.random() < 0.1 ? '轻微淤青，已建议冰敷' : undefined,
          });
        }
      }
    }

    records.push({
      id: `rec_${String(i + 1).padStart(4, '0')}`,
      recordNo,
      customerId: `cust_${String(1000 + i).padStart(4, '0')}`,
      customerName,
      customerGender: pickRandom(['female', 'female', 'female', 'male']),
      customerAge,
      customerPhone: generatePhone(),
      storeId,
      storeName: storeNameMap[storeId],
      doctorId,
      doctorName: doctorNameMap[doctorId],
      nurseId: 'user_006',
      nurseName: '王小护',
      projectId,
      projectName: projInfo.name,
      projectCategory: projInfo.category,
      injectionPoints,
      drugs,
      preOpAssessment: '客户身体健康，无禁忌症，符合注射条件',
      intraOpNotes: '注射过程顺利，客户配合良好，无不适',
      postOpNotes: '术后观察30分钟，无异常反应，已告知注意事项',
      adverseReactions: Math.random() < 0.15 ? ['注射部位轻微红肿'] : undefined,
      status,
      reviewerId: status !== 'pending' ? 'user_008' : undefined,
      reviewerName: status !== 'pending' ? '质控张' : undefined,
      reviewRemark: status === 'rejected' ? '记录不完整，缺少术前照片' : status === 'reviewing' ? '正在复核药品批次信息' : undefined,
      reviewedAt: status !== 'pending' ? addDays(formatDateTime(startDate), 1) : undefined,
      appointmentDate: formatDate(recordDate),
      startTime: formatDateTime(startDate),
      endTime: formatDateTime(endDate),
      totalAmount,
      paidAmount,
      paymentStatus: paidStatus,
      createdAt: formatDateTime(startDate),
      updatedAt: formatDateTime(new Date()),
      followUps,
      qualityScore,
      riskPoints,
    });
  }

  return records;
}

